# Multiple Linear Regression

# Importing the dataset
dataset = read.csv('50_Startups.csv')

# Encoding categorical data
dataset$State = factor(dataset$State,
                       levels = c('New York', 'California', 'Florida'),
                       labels = c(1, 2, 3))

# Splitting the dataset into the Training set and Test set
# install.packages('caTools')
library(caTools)
set.seed(123)
split = sample.split(dataset$Profit, SplitRatio = 0.8)
training_set = subset(dataset, split == TRUE)
test_set = subset(dataset, split == FALSE)

# Feature Scaling ===> not needed as it is included in the method below.

# Fitting Multiple Linear Regression to the Training set
regressor = lm(formula = Profit ~ .,  # Automatically removes on dummy var
               data = training_set)  # Check regressor with summary()

# Instead of using '.' for all the columns, we can manually import them:
# regressor = lm(formula = Profit ~ R.D.Spend + Administration + Marketing.Spend + State,
#                data = training_set)

# Some special characters like space and &, will be replaced by dot '.'
# at the dataset imported by R. Check to confirm always!

# Predicting the Test set results
y_pred = predict(regressor, newdata = test_set)

